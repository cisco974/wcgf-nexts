			<h3>Rules of Tarot for 4 Players</h3>
			<p>The game of Tarot, known for its strategic and dynamic gameplay, is a classic French card game. Dating back to the 16th century, it is primarily played with four players, though it can also be played with three or five. The goal is to accumulate the highest number of points. This guide presents the Tarot cards and rules for four players.</p>

			<h4>Composition of the Deck</h4>
			<p>Tarot is played with 78 cards:</p>
			<ul>
				<li>56 traditional suit cards (Spades, Diamonds, Clubs, Hearts), each consisting of 14 cards from Ace to King.</li>
				<li>21 Trumps, numbered and stronger than suits. The higher the number, the stronger the trump.</li>
				<li>The Fool, marked with a star, acts as a joker.</li>
			</ul>
			<p>The key cards in the game are the Oulders or Bouts (1, 21, and the Fool).</p>

			<h4>Card Values</h4>
			<p>Knowing the value of the cards is essential:</p>
			<ul>
				<li>1 Oudler (1, 21, Fool) = 5 points</li>
				<li>1 King = 5 points</li>
				<li>1 Queen = 4 points</li>
				<li>1 Knight = 3 points</li>
				<li>1 Jack = 2 points</li>
				<li>Other cards = 0.5 points</li>
			</ul>
			<p>Low cards are counted in pairs.</p>

			<h4>Objective of the Game</h4>
			<p>The goal for the "taker" is to score a set number of points, helped by the Oulders (Trump 21, Trump 1, and the Fool). Each Oudler is worth 5 points:</p>
			<ul>
				<li>Without any Oudler, 56 points are needed.</li>
				<li>With 1 Oudler, 51 points are needed.</li>
				<li>With 2 Oulders, 41 points are enough.</li>
				<li>With 3 Oulders, only 36 points are required.</li>
			</ul>

			<h4>Gameplay</h4>
			<p>Cards are dealt in sets of three after selecting the dealer. The "chien" (dog) is a set of 6 cards set aside. If a player decides to take, they add these cards to their hand, replacing 6 cards of their choice, except Kings and Oulders.</p>

			<h4>The Bids</h4>
			<p>The player who takes makes a bid, influencing the points won or lost:</p>
			<ul>
