			<h4>Announcements and Point Distribution</h4>
			<p>Players can announce a "handful" of trumps for additional points. Points are distributed based on the success or failure of the bid, proportionally between the taker and their partner. If the taker plays alone, they claim all the points in case of victory.</p>

