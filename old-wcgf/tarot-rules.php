			<h4>Card Distribution</h4>
			<p>The deck consists of 56 traditional cards, ranging from Ace to King, with an additional card, the Knight, placed between the Jack and Queen. Additionally, there are 21 trumps numbered 1 to 21, used to cut other suits, and the Excuse, acting as a joker.</p>

			<h4>Objective of the Game</h4>
			<p>The main goal is to accumulate as many points as possible. This is achieved through successive bids, where the player, aided by the Oudlers, aims to reach a specific score to win the bid.</p>

